/*******************************************************************************
 * FILE:         trig.c
 * MODULE:       Core/Math
 * DESCRIPTION:  Definition of the 256-entry fixed-point sine lookup table.
 *               This translation unit exists solely to give psx_sin_lut a
 *               single home in the binary. The table is stored in ROM (linked
 *               to the .rodata section) so it consumes zero bytes of precious
 *               main RAM at runtime.
 *
 *               TABLE GENERATION:
 *                 Each entry = round(sin(i * 2 * PI / 256) * 4096)
 *                 Scale factor: FP4_12_ONE = 4096
 *                 Entry type: int16_t (signed 16-bit, range -4096 to +4096)
 *                 Total size: 256 * 2 = 512 bytes in .rodata
 *
 *               The table is symmetric: entries 0-64 are the first quadrant,
 *               65-128 mirror the first quadrant descending, 129-192 are
 *               the negative mirror, and 193-255 mirror that back up.
 *               This symmetry was used to verify the values by inspection.
 *
 * DEPENDENCIES: trig.h (for extern declaration)
 *******************************************************************************/

